USE NexusPay1;
-- Active Users --
SELECT 
    COUNT(*) AS total_users,
    SUM(account_status = 'active') AS active_users
FROM Users;

-- KYC Status Distribution --
SELECT 
    kyc_status,
    COUNT(*) AS total_users
FROM Users
GROUP BY kyc_status;

-- Total Wallet Balance in System --
SELECT 
    SUM(balance) AS total_wallet_balance
FROM Wallets;

/*Total Transactions & Success Rate*/
SELECT 
    COUNT(*) AS total_transactions,
    SUM(transaction_status = 'success') AS successful_txn,
    ROUND(SUM(transaction_status = 'success') / COUNT(*) * 100, 2) AS success_rate
FROM Transactions;

/*Daily Transaction Trend*/
SELECT 
    DATE(created_at) AS txn_date,
    COUNT(*) AS total_txn,
    SUM(amount) AS total_amount
FROM Transactions
GROUP BY txn_date
ORDER BY txn_date;

/*Top 5 Users by Transaction Amount*/
SELECT 
    u.user_id,
    u.full_name,
    SUM(t.amount) AS total_spent
FROM Transactions t
JOIN Wallets w ON t.sender_wallet_id = w.wallet_id
JOIN Users u ON w.user_id = u.user_id
WHERE t.transaction_status = 'success'
GROUP BY u.user_id
ORDER BY total_spent DESC
LIMIT 5;

/*Merchant-wise Revenue*/
SELECT 
    m.merchant_name,
    SUM(r.amount) AS total_revenue
FROM Revenue r
JOIN Merchants m ON r.merchant_id = m.merchant_id
GROUP BY m.merchant_name
ORDER BY total_revenue DESC;

/*Transaction Type Distribution*/
SELECT 
    txn_type,
    COUNT(*) AS total_txn,
    SUM(amount) AS total_amount
FROM Transactions
GROUP BY txn_type;

/*Fraud Detection Summary*/
SELECT 
    fraud_status,
    COUNT(*) AS cases,
    AVG(risk_score) AS avg_risk_score
FROM FraudDetection
GROUP BY fraud_status;

/*High Risk Transactions (Fraud Alert)*/
SELECT 
    f.txn_id,
    f.user_id,
    f.risk_score,
    f.action_taken
FROM FraudDetection f
WHERE f.risk_score > 80;

/*Revenue by Type*/
SELECT 
    revenue_type,
    SUM(amount) AS total_revenue
FROM Revenue
GROUP BY revenue_type;

/*Support Ticket Status Overview*/
SELECT 
    status,
    COUNT(*) AS total_tickets
FROM SupportTickets
GROUP BY status;

/*Average Transaction Value*/
SELECT 
    ROUND(AVG(amount), 2) AS avg_transaction_value
FROM Transactions
WHERE transaction_status = 'success';


