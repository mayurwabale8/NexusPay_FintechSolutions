create database NexusPay1;

use NexusPay1;

CREATE TABLE Users (
    user_id INT PRIMARY KEY AUTO_INCREMENT,
    full_name VARCHAR(100) NOT NULL,
    phone VARCHAR(15) UNIQUE NOT NULL,
    email VARCHAR(100) UNIQUE NOT NULL,
    address TEXT NOT NULL,
    login_pin_hash VARCHAR(255) NOT NULL,
    transaction_pin_hash VARCHAR(255) NOT NULL,
    account_status ENUM('active', 'inactive', 'suspended') DEFAULT 'active',
    kyc_status ENUM('verified', 'pending', 'rejected') DEFAULT 'pending',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- Get all active users
SELECT * FROM Users WHERE account_status = 'active';

-- Count users by KYC status
SELECT kyc_status, COUNT(*) FROM Users GROUP BY kyc_status;

-- Suspended users
SELECT full_name, email FROM Users 
WHERE account_status = 'suspended';

CREATE TABLE Wallets (
    wallet_id INT PRIMARY KEY AUTO_INCREMENT,
    user_id INT UNIQUE,
    balance DECIMAL(12 , 2 ) DEFAULT 0.00,
    upi_handle VARCHAR(50) UNIQUE,
    is_active BOOLEAN DEFAULT TRUE,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (user_id) REFERENCES Users (user_id) ON DELETE CASCADE
);

-- Check wallet balances above 50,000
SELECT * FROM Wallets WHERE balance > 50000;

-- Total balance in system
SELECT SUM(balance) AS total_balance FROM Wallets;

-- Inactive wallets
SELECT * FROM Wallets WHERE is_active = FALSE;

-- Wallet with highest balance
SELECT * FROM Wallets ORDER BY balance DESC LIMIT 1;

CREATE TABLE BankAccounts (
    bank_id INT PRIMARY KEY AUTO_INCREMENT,
    user_id INT,
    bank_name VARCHAR(100),
    account_number VARCHAR(30) UNIQUE,
    ifsc_code VARCHAR(15),
    is_primary BOOLEAN DEFAULT FALSE,
    wallet_id INT,
FOREIGN KEY (user_id) REFERENCES Users(user_id) ON DELETE CASCADE,
FOREIGN KEY (wallet_id) REFERENCES Wallets(wallet_id) ON DELETE CASCADE
);

-- Primary bank accounts
SELECT * FROM BankAccounts WHERE is_primary = TRUE;

-- Count accounts per bank
SELECT bank_name, COUNT(*) FROM BankAccounts GROUP BY bank_name;



CREATE TABLE Merchants (
    merchant_id INT PRIMARY KEY AUTO_INCREMENT,
    merchant_name VARCHAR(100),
    category VARCHAR(50),
    owner_user_id INT,
     bank_id INT,
    FOREIGN KEY (owner_user_id) REFERENCES Users(user_id),
FOREIGN KEY (bank_id) REFERENCES BankAccounts(bank_id) 
);

-- All e-commerce merchants
SELECT * FROM Merchants WHERE category = 'E-commerce';

-- Count merchants per category
SELECT category, COUNT(*) FROM Merchants GROUP BY category;

-- Merchants with owners
SELECT merchant_name, owner_user_id FROM Merchants;

CREATE TABLE Transactions (
    txn_id INT PRIMARY KEY AUTO_INCREMENT,
    txn_ref VARCHAR(50) UNIQUE NOT NULL,
    sender_wallet_id INT,
    receiver_wallet_id INT,
    bank_id INT,
    merchant_id INT,
    txn_type ENUM('send', 'request', 'bill_pay', 'cashback'),
    amount DECIMAL(12,2) NOT NULL,
    fee DECIMAL(8,2) DEFAULT 0.00,
    net_amount DECIMAL(12,2) GENERATED ALWAYS AS (amount - fee) STORED,
    transaction_status ENUM('processing', 'success', 'failed') DEFAULT 'processing',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (sender_wallet_id) REFERENCES Wallets(wallet_id),
    FOREIGN KEY (receiver_wallet_id) REFERENCES Wallets(wallet_id),
    FOREIGN KEY (bank_id) REFERENCES BankAccounts(bank_id),
    FOREIGN KEY (merchant_id) REFERENCES Merchants(merchant_id)
);

-- Successful transactions
SELECT * FROM Transactions WHERE transaction_status = 'success';

-- Total transaction amount
SELECT SUM(amount) FROM Transactions;

-- Failed transactions
SELECT * FROM Transactions WHERE transaction_status = 'failed';

-- Top 5 highest transactions
SELECT * FROM Transactions ORDER BY amount DESC LIMIT 5;

-- Transactions with fees > 10
SELECT * FROM Transactions WHERE fee > 10;

CREATE TABLE TransactionHistory (
    history_id INT PRIMARY KEY AUTO_INCREMENT,
    txn_id INT,
    old_status VARCHAR(50),
    new_status VARCHAR(50),
    action_type VARCHAR(50),
    performed_by VARCHAR(20),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (txn_id) REFERENCES Transactions (txn_id) ON DELETE CASCADE
);

-- All history logs
SELECT * FROM TransactionHistory;

-- Status change tracking
SELECT txn_id, old_status, new_status 
FROM TransactionHistory;

-- Count actions by type
SELECT action_type, COUNT(*) 
FROM TransactionHistory GROUP BY action_type;

CREATE TABLE FraudDetection (
    fraud_id INT PRIMARY KEY AUTO_INCREMENT,
    txn_id INT,
    user_id INT,
    rule_name VARCHAR(100),
    rule_type ENUM('amount', 'velocity', 'location', 'device'),
    risk_score INT,
    action_taken ENUM('flag', 'block', 'verify'),
    fraud_status ENUM('flagged', 'confirmed', 'cleared') DEFAULT 'flagged',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (txn_id) REFERENCES Transactions (txn_id),
    FOREIGN KEY (user_id) REFERENCES Users (user_id)
);

-- High risk transactions (risk_score > 80)
SELECT * FROM FraudDetection WHERE risk_score > 80;

-- Count by fraud status
SELECT fraud_status, COUNT(*) 
FROM FraudDetection GROUP BY fraud_status;

-- Blocked transactions
SELECT * FROM FraudDetection WHERE action_taken = 'block';

CREATE TABLE Services (
    service_id INT PRIMARY KEY AUTO_INCREMENT,
    user_id INT,
    merchant_id INT,
    bank_id INT,
    service_type ENUM('coupon', 'discount', 'loan', 'cashback', 'autopay'),
    service_name VARCHAR(100),
    amount DECIMAL(12 , 2 ),
    interest_rate DECIMAL(5 , 2 ),
    frequency ENUM('weekly', 'monthly'),
    next_due_date DATE,
    service_status ENUM('active', 'used', 'expired', 'paused') DEFAULT 'active',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (user_id) REFERENCES Users (user_id),
    FOREIGN KEY (merchant_id) REFERENCES Merchants (merchant_id),
    FOREIGN KEY (bank_id) REFERENCES BankAccounts (bank_id)
);

-- Active services
SELECT * FROM Services WHERE service_status = 'active';

-- Services expiring soon
SELECT * FROM Services 
WHERE next_due_date <= CURDATE() + INTERVAL 7 DAY;

-- Count services by type
SELECT service_type, COUNT(*) 
FROM Services GROUP BY service_type;


CREATE TABLE Revenue (
    revenue_id INT PRIMARY KEY AUTO_INCREMENT,
    txn_id INT,
    user_id INT,
    merchant_id INT,
    service_id INT,
    revenue_type ENUM(
        'transaction_fee',
        'merchant_commission',
        'loan_interest',
        'autopay_fee'),
    amount DECIMAL(12,2),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (txn_id) REFERENCES Transactions(txn_id),
    FOREIGN KEY (user_id) REFERENCES Users(user_id),
    FOREIGN KEY (merchant_id) REFERENCES Merchants(merchant_id),
    FOREIGN KEY (service_id) REFERENCES Services(service_id)
);

-- Total revenue
SELECT SUM(amount) FROM Revenue;

-- Revenue by type
SELECT revenue_type, SUM(amount) 
FROM Revenue GROUP BY revenue_type;

-- Revenue per merchant
SELECT merchant_id, SUM(amount) 
FROM Revenue GROUP BY merchant_id;

CREATE TABLE SupportTickets (
    ticket_id INT PRIMARY KEY AUTO_INCREMENT,
    user_id INT,
    txn_id INT,
    issue_type VARCHAR(50),
    description TEXT,
    status ENUM('open', 'in_progress', 'resolved') DEFAULT 'open',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    resolved_at TIMESTAMP NULL,
    FOREIGN KEY (user_id) REFERENCES Users(user_id),
    FOREIGN KEY (txn_id) REFERENCES Transactions(txn_id)
);
-- Open tickets
SELECT * FROM SupportTickets WHERE status = 'open';

-- Resolved tickets
SELECT * FROM SupportTickets WHERE status = 'resolved';

-- Tickets per issue type
SELECT issue_type, COUNT(*) 
FROM SupportTickets GROUP BY issue_type;

CREATE TABLE Reviews (
    review_id INT PRIMARY KEY AUTO_INCREMENT,
    user_id INT,
    txn_id INT,
    rating INT CHECK (rating BETWEEN 1 AND 5),
    feedback TEXT,
    review_type ENUM('support', 'transaction', 'app'),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (user_id) REFERENCES Users(user_id),
    FOREIGN KEY (txn_id) REFERENCES Transactions(txn_id)
);
-- Average rating
SELECT AVG(rating) FROM Reviews;

-- Reviews with low rating (<=2)
SELECT * FROM Reviews WHERE rating <= 2;

-- Count reviews by type
SELECT review_type, COUNT(*) 
FROM Reviews GROUP BY review_type;

